package JavaAssignments;

public class Switch_case_program {

	public static void main(String[] args) 
	{
		switch(8)
		{
		case 1:
		
			System.out.println("For English");
			 break;
		
		
		case 2:
			
			System.out.println("For Marathi");
			 break;	
		
		
		case 3:
			
			System.out.println("For Hindi");
			 break;	
		
		
		case 4:
			
			System.out.println("For German");
			 break;	
		
		default:
		{
			System.out.println("Your selection is wrong please select 1-4");
		}
	}

	}
}
