package JavaAssignments;

public class Global_variable5methodsoneclass 
{
	
  static int a=60;
  static int b=30;
	
    static void add()
	{
		System.out.println(a+b);
	}
    static void sub()
    {
    	System.out.println(a-b);
    }
    static void mul()
    {
    	System.out.println(a*b);
    }
    static void div()
    {
    	System.out.println(a/b);
    }
    static void mod()
    {
    	System.out.println(a%b);
    	
    }
	public static void main(String[] args)
	{
		
		add();
		sub();
		mul();
		div();
		mod();
		
	}

}
